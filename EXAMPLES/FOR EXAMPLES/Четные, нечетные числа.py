a = [1,2,3,4,32,4,5,3,5]

even = []
odd = []

n = len(a)

for i in range(n):
    if a[i] % 2 == 0:
        even.append(i)
    else:
        odd.append(i)

print("Четные числа на индексах:", even)
print("Нечетные числа на индексах:", odd)

# Четные числа на индексах: [1, 3, 4, 5]
# Нечетные числа на индексах: [0, 2, 6, 7, 8]

